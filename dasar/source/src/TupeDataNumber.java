public class TupeDataNumber {
	public static void main(String[] args) {
		System.out.println("ini adalah tipe data angka");
		byte a = 127;
		int b = 2000;
		short c = 1270;
		long d = 123450;
		long e = 123450L;

		System.out.println("ini adalah byte"+a);
		System.out.println("ini adalah int "+b);
		System.out.println("ini adalah short "+c);
		System.out.println("ini adalah long "+d);
		System.out.println("ini adalah long "+e);
		System.out.println();
		System.out.println();
		System.out.println("ini adalah Desimal ");

		float g = 3.4f;
		double h = 12.12;
		System.out.println("ini adalah float "+g);
		System.out.println("ini adalah double"+h);

	}
}

